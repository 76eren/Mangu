package com.example.mangareader.Favourites;


public class FavouriteItem {
    public String source;
    public String url;
    public String image;
    public String mangaName;

    public FavouriteItem(String source, String url, String image, String mangaName) {
        this.source = source;
        this.url = url;
        this.image = image;
        this.mangaName = mangaName;
    }
}
